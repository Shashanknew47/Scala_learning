
object ValueVariableTypes extends App {

    // val is here like cont in JavaScript. You can't change val values.
    val integer: Int = 12;
    println(integer);
    
    val s: String = "Hello Scala"
    println(s)

    val b:Boolean = true;


    // var is like a normal nor variable. which can be changed. 

    var f:Float = 34.4F;  // Make sure to apply F infront of float
    println(f);


    var big_integer:Long = 94444573975947759L;
    println(big_integer)


  
 
}